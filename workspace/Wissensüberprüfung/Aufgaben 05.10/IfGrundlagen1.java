
public class IfGrundlagen1 {

	public static void main(String[] args) {

		int intValue = 12;

		if (intValue == 12) {
			System.out.println("Der Wert ist 12");
		} else {
			System.out.println("Der Wert ist nicht 12");
		}

		if (intValue > 10) {
			System.out.println("Der Wert ist größer 10");
		} else {
			System.out.println("Der Wert ist nicht größer 10");
		}

		if (intValue >= 12) {
			System.out.println("Der Wert ist größer gleich 12");
		} else {
			System.out.println("Der Wert ist nicht größer gleich 12");
		}
	}
}
